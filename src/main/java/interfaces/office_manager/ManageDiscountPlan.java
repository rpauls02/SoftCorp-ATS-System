    private void findPlanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findButtonActionPerformed
        DefaultTableModel model = (DefaultTableModel)discountPlanInfoTable.getModel();
        Connection conn = null;
        PreparedStatement pstm = null;
        ResultSet rs = null;
        try {
            conn = DBConnection.getConnection();
            String query = "SELECT * FROM discountplan1 WHERE customerUsername = ?";
            pstm = conn.prepareStatement(query);
            pstm.setString(1, customerIDField.getText());
            rs = pstm.executeQuery();
            if (!rs.next()){
                JOptionPane.showMessageDialog(this, "Specified customer could not be found. " +
                        "Review entered details or contact system administrator");
            } else {
                model.setRowCount(0);
                do {
                    //"Username", "Plan", "Fixed Rate", "Flexible Rate 1", "Flexible Rate 2", "Flexible Rate 3"
                    Object[] row = new Object[6];
                    row[0] = rs.getString("customerUsername");
                    row[1] = rs.getString("type");
                    row[2] = rs.getString("fixRate");
                    row[3] = rs.getString("flexRate1");
                    row[4] = rs.getString("flexRate2");
                    row[5] = rs.getString("flexRate3");
                    model.addRow(row);
                } while (rs.next());
            }
        } catch (SQLException sqle) {
            if (conn != null) { try { conn.rollback(); } catch (SQLException e) { throw new RuntimeException(sqle); }}
        } finally {
            try { if (conn != null) conn.close(); } catch (Exception e) { throw new RuntimeException(e); }
            try { if (pstm != null) pstm.close(); } catch (Exception e) { throw new RuntimeException(e); }
            try { if (rs != null) rs.close(); } catch (Exception e) { throw new RuntimeException(e); }
        }
    }//GEN-LAST:event_findButtonActionPerformed

